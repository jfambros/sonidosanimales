package com.amber.utils;

public class Animal {
	private String nombreAnimal;
	private String drawableSonidoAnimal;
	private String idioma;

	public String getNombreAnimal() {
		return nombreAnimal;
	}
	public void setNombreAnimal(String nombreAnimal) {
		this.nombreAnimal = nombreAnimal;
	}
	public String getDrawableSonidoAnimal() {
		return drawableSonidoAnimal;
	}
	public void setDrawableSonidoAnimal(String drawableSonidoAnimal) {
		this.drawableSonidoAnimal = drawableSonidoAnimal;
	}
	public String getIdioma() {
		return idioma;
	}
	public void setIdioma(String idioma) {
		this.idioma = idioma;
	}


}

package com.amber.utils;

import android.widget.ImageView;
import android.widget.TextView;

public class DatosAnimalView {
	private ImageView imagenAnimal;
	private TextView nombreAnimal;
	
	public ImageView getImagenAnimal() {
		return imagenAnimal;
	}
	public void setImagenAnimal(ImageView imagenAnimal) {
		this.imagenAnimal = imagenAnimal;
	}
	public TextView getNombreAnimal() {
		return nombreAnimal;
	}
	public void setNombreAnimal(TextView nombreAnimal) {
		this.nombreAnimal = nombreAnimal;
	}

}

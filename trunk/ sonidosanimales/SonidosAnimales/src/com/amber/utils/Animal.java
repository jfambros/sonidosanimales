package com.amber.utils;

public class Animal {
	private String numeroAnimal;
	private String nombreAnimal;
	private String drawableAnimal;
	private String sonidoAnimal;
	
	public String getNumeroAnimal() {
		return numeroAnimal;
	}
	public void setNumeroAnimal(String numeroAnimal) {
		this.numeroAnimal = numeroAnimal;
	}
	public String getNombreAnimal() {
		return nombreAnimal;
	}
	public void setNombreAnimal(String nombreAnimal) {
		this.nombreAnimal = nombreAnimal;
	}
	public String getDrawableAnimal() {
		return drawableAnimal;
	}
	public void setDrawableAnimal(String drawableAnimal) {
		this.drawableAnimal = drawableAnimal;
	}
	public String getSonidoAnimal() {
		return sonidoAnimal;
	}
	public void setSonidoAnimal(String sonidoAnimal) {
		this.sonidoAnimal = sonidoAnimal;
	}
	

}
